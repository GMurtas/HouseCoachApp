package com.example.giuliam.housecoach;

public class ItemGadget {
    int image;

    String product;

    public ItemGadget(int image, String product) {
        this.image = image;
        this.product = product;
    }

    public int getImage() {
        return image;
    }

    public String getProduct() {
        return product;
    }
}
